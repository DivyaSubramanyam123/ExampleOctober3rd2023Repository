package com.sgtesting.ArrayAssignment;
/*
 * Create 3*3 char 2D array,Read First column elements
 */
public class TwoDCharArrayFirstColumn {

	public static void main(String[] args) {
		char ch[][]= {{'H','D','U'},{'S','S','E'},{'H','R','A'}};
		for (int i=0;i<ch.length;i++)
		{
			for(int j=0;j<=0;j++)
			{
				System.out.println(ch[i][j]);
			}
		}

	}

}
